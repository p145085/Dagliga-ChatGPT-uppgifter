#23. **Sortera strängar efter längd:** Bygg ett program som sorterar en lista av strängar baserat på deras längd.

# Lista med strängar
strings = ["äpple", "banan", "kiwi", "ananas", "apelsin"]

# Sortera listan efter längden på strängarna
sorted_strings = sorted(strings, key=len)

# Skriv ut den sorterade listan
print(sorted_strings)
